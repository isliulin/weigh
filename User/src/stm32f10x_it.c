void USART1_IRQHandler(void)                	//串口1中断服务程序
{
    static u8 Res[100], len = 0;
    
    if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
    {
        Res[len++] = USART_ReceiveData(USART1);
        if(Res[0] == MB_ADDR)
        {
            MbRicv.DataRicv[len - 1] = Res[len - 1];

            if(len >= 8)
            {
                if(Res[1] == 16)                //0x16
                {
                    if(len >= (Res[6] + 9))
                    {
                        len = 0;
                        MbStatus.MbRicved = 1;
                    }
                }
                else
                {
                    len = 0;
                    MbStatus.MbRicved = 1;
                }
            } 
        }
        else
        {
            len = 0;
            MbStatus.MbRicved = 0;
        }
    }
} 

